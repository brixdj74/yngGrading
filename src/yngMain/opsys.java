    public void table(){
        try{
            pst = conn.prepareStatement("select * from schedule inner join course on course.courseId = schedule.courseId inner join subjects on subjects.subjectId = schedule.subjectId inner join employee on employee.employeeId = schedule.employeeId where schedule.employeeId = ?");
            pst.setString(1,id.getText());
            rs = pst.executeQuery();
            java.sql.ResultSetMetaData rsmd = rs.getMetaData();
            int n = rsmd.getColumnCount();
            DefaultTableModel dtm=(DefaultTableModel) subjects.getModel();
            dtm.setRowCount(0);
            while (rs.next()){
                Vector v = new Vector();
                for(int i=1;i<=n;i++){
                    v.add(rs.getString("scheduleId"));
                    v.add(rs.getString("description"));
                    v.add(rs.getString("course"));
                    v.add(rs.getString("schedule.year"));
                    v.add(rs.getString("subjectId"));
                  
                }
                dtm.addRow(v);
            }
            
            
        }catch (SQLException ex) {
           System.out.println(ex);
        }
    }

