    private void subjectsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_subjectsMouseClicked
            DefaultTableModel dtm = (DefaultTableModel)subjects.getModel();
            int si = subjects.getSelectedRow();
            String c = dtm.getValueAt(si, 0).toString();
            suid.setText(c);
            
            try{
                pst = conn.prepareStatement("select * from subjects where subjectId = ?");
                pst.setString(1,dtm.getValueAt(si,4).toString());
                
                rs = pst.executeQuery();
                
                if(rs.next()){
                    subid.setText(rs.getString("subjectId"));
                    subjectCode = rs.getString("code");
                    subjectDesc = rs.getString("description");
                }
            }catch(SQLException e){
                System.out.println(e);
            }
    }//GEN-LAST:event_subjectsMouseClicked

